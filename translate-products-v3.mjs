// We'll dynamically load v2 by importing it
// But since v2 runs main(), we need to parse differently
// Instead, let's just inject our supplement into v2

// Strategy: read v2 dictionaries, add our supplement, then translate
// Simpler approach: rewrite translateText to use merged dicts

console.log('Merging supplement dictionaries into v2...');

// Read and modify the v2 script to add our supplement entries
let modified = v2Source;

for (const lang of LANGUAGES) {
  if (!supplementDictionaries[lang]) continue;
  
  // Find the end of the language's dictionary array in v2
  // Pattern: the ], before the next language section or end
  const langPattern = new RegExp(`(${lang}:\\s*\\[)`, 'g');
  const match = langPattern.exec(modified);
  if (!match) {
    console.log(`  Warning: Could not find ${lang} dictionary in v2`);
    continue;
  }
  
  // Find the matching closing ]
  let depth = 0;
  let pos = match.index + match[0].length;
  let closingPos = -1;
  for (let i = pos; i < modified.length; i++) {
    if (modified[i] === '[') depth++;
    if (modified[i] === ']') {
      if (depth === 0) { closingPos = i; break; }
      depth--;
    }
  }
  
  if (closingPos === -1) {
    console.log(`  Warning: Could not find end of ${lang} dictionary`);
    continue;
  }
  
  // Build new entries string
  const newEntries = supplementDictionaries[lang].map(([en, loc]) => {
    const escEn = en.replace(/'/g, "\\'");
    const escLoc = loc.replace(/'/g, "\\'");
    return `  ['${escEn}', '${escLoc}']`;
  }).join(',\n');
  
  // Insert before the closing ]
  modified = modified.substring(0, closingPos) + ',\n' + newEntries + '\n' + modified.substring(closingPos);
  
  console.log(`  ${lang}: added ${supplementDictionaries[lang].length} supplement entries`);
}

// Write the enhanced v2 script
const outputPath = path.join(__dirname, 'translate-products-v2-enhanced.mjs');
fs.writeFileSync(outputPath, modified, 'utf-8');
console.log(`\nWrote enhanced script to: translate-products-v2-enhanced.mjs`);
console.log('Run: node translate-products-v2-enhanced.mjs all');
